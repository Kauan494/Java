    //Pega os filmes do banco e coloca dentro do JList
    public void carregarLista(){
        javax.swing.DefaultListModel modelo = new javax.swing.DefaultListModel();
        ArrayList<String> filmes = controle.buscarPlay(tipoPlaylist);
        for(String filme : filmes){
            modelo.addElement(filme);
        }
        listaFilmes.setModel(modelo);
    }

