    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        //Mostra uma janela de confirmação 
        int resposta = javax.swing.JOptionPane.showConfirmDialog(this,"Deseja excluir toda a lista?","Confirmar",
        javax.swing.JOptionPane.YES_NO_OPTION);
        
        if(resposta ==  javax.swing.JOptionPane.YES_NO_OPTION){
            controle.excluirLista(tipoPlaylist);
            carregarLista();
            
            javax.swing.JOptionPane.showMessageDialog(this, "Lista excluida!");
        }
    }//GEN-LAST:event_btn_excluirActionPerformed

    private void btn_removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removerActionPerformed
        String filmeSelecionado = listaFilmes.getSelectedValue();//Pega o item selecionado no jlist
        
        if (filmeSelecionado == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione um filme!");
            return;
        }
        
        controle.removerInteracao(filmeSelecionado, tipoPlaylist);
        carregarLista();
    }//GEN-LAST:event_btn_removerActionPerformed

